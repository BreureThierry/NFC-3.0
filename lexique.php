                    Les merchandise correspondent aux vêtements et accessoires d’une collection NFT, que l’on peut acheter si on est membre
                    </p>
                </li>

                <li>
                    <a href="#" class="lexique_mot">MÉTADONNÉES</a>
                    <p class="lexique_definition">
                    Composées de quelques lignes de texte, les métadonnées décrivent l’œuvre (auteur, titre, date de création, numéro de série, attributs, hash…) et contiennent un lien vers son emplacement physique.
                    La méthode de stockage des métadonnées est déterminée par l’artiste ou par la plate-forme de vente
                    </p>
                </li>

                <li>
                    <a href="#" class="lexique_mot">METAMASK</a>
                    <p class="lexique_definition">
                    Le plus courant des portefeuilles de crypto-monnaies, compatible NFT et applications décentralisées
                    </p>
                </li>

                <li>
                    <a href="#" class="lexique_mot">MÉTAVERS</a>
                    <p class="lexique_definition">
                    Univers virtuel en 3D, où l’on peut notamment utiliser et exposer ses NFT. Les plus connus sont The SandBox et Decentraland
                    </p>
                </li>

                <li>
                    <a href="#" class="lexique_mot">MiCA</a>
                    <p class="lexique_definition">
                    C’est un projet de règlement émis par l’Union européenne pour encadrer les transactions en cryptomonnaies. Il vient harmoniser la régulation des actifs virtuels au niveau européen. Il s’accompagne d’un autre règlement, le TFR
                    </p>
                </li>

                <li>
                    <a href="#" class="lexique_mot">MINAGE</a>
                    <p class="lexique_definition">
                    De nombreuses blockchains, dont Bitcoin, utilisent ce processus qui consiste à résoudre un défi informatique pour valider les blocs. L’utilisation de puissance de calcul informatique permet de traiter des transactions et de sécuriser le réseau. Le minage est récompensé par la distribution de nouveaux bitcoins/altcoins. Le processus de minage est connu sous le nom Proof of Work (PoW). Il est souvent décrié pour son caractère énergivore
                    </p>
                </li>

                <li>
                    <a href="#" class="lexique_mot">MINT</a>
                    <p class="lexique_definition">
                    Processus de création d’un NFT. Le fait de l’inscrire dans la blockchain : équivalent de frapper une pièce de monnaie
                    </p>
                </li>

                <li>
                    <a href="#" class="lexique_mot">MOON</a>
                    <p class="lexique_definition">
                    Dire qu’un NFT est en train de « mooner » signifie que le prix du NFT en question s’envole
                    </p>
                </li>

                <li>
                    <h3 class="lexique_alphabet">N</h3>

                    <a href="#" class="lexique_mot">NFT</a>
                    <p class="lexique_definition">
                    Non fongible token - Jeton Non Fongible en français. Titre de propriété d’un objet numérique inscrit dans la blockchain, impossible à dupliquer et à effacer
                    </p>
                </li>

                <li>
                    <a href="#" class="lexique_mot">NFT GÉNÉRATIF</a>
                    <p class="lexique_definition">
                    On parle de NFT ou Art génératif pour désigner des pièces créées aléatoirement grâce à un algorithme. La collection d’art génératif la plus connue est celle des Crypto-punks
                    </p>
                </li>

                <li>
                    <a href="#" class="lexique_mot">NODE</a>
                    <p class="lexique_definition">
                    Acteur de la blockchain qui vérifie les transactions et donc sécurise la blockchain. Participe à la décentralisation
                    </p>
                </li>

                <li>
                    <h3 class="lexique_alphabet">O</h3>
                
                    <a href="#" class="lexique_mot">OFF CHAIN</a>
                    <p class="lexique_definition">
                    Off Chain signifie en dehors de la blockchain
                    </p>
                </li>

                <li>
                    <a href="#" class="lexique_mot">OG</a>
                    <p class="lexique_definition">
                    Original Gangster. Quelqu’un qui est dans le « game » depuis longtemps et qui a une réputation
                    </p>
                </li>

                <li>
                    <a href="#" class="lexique_mot">OPEN AUCTION</a>
                    <p class="lexique_definition">
                    Désigne une enchère « classique ». Sur la plupart des plateformes, les enchères ont une date et une heure de fin. Avant cette date, vous pouvez “bider” (enchérir). Vous ne pouvez bider qu’un montant supérieur au dernier bid
                    </p>
                </li>

                <li>
                    <a href="#" class="lexique_mot">OPEN EDITION</a>
                    <p class="lexique_definition">
                    Séries limitées appliquées au monde numérique. Lors d’une Open Editions, une œuvre sera éditée en autant d’exemplaires qu’elle trouvera d’acheteurs dans un temps donné
                    </p>
                </li>

                <li>
                    <a href="#" class="lexique_mot">OPENSEA</a>
                    <p class="lexique_definition">
                    La plus grande marketplace de NFT
                    </p>
                </li>

                <li>
                    <h3 class="lexique_alphabet">P</h3>

                    <a href="#" class="lexique_mot">PANIC SELL</a>
                    <p class="lexique_definition">
                    Le « panic sell » (vente panique) consiste à vendre une cryptomonnaie dans l’urgence par peur que le cours ne s’effondre. Cela se produit souvent sur le marché des cryptomonnaies dès qu’une mauvaise nouvelle est annoncée, ou que la chute d’un cours s’accélère brutalement
                    </p>
                </li>

                <li>
                    <a href="#" class="lexique_mot">PAPER HANDS</a>
                    <p class="lexique_definition">
                    Contraire de diamond hand c’est des personnes qui vendent trop tôt dès que le prix baisse 
                    </p>
                </li>

                <li>
                    <a href="#" class="lexique_mot">PFP</a>
                    <p class="lexique_definition">
                    Profile picture – Photo de profil. On entend souvent parler des projets pfp il s’agit de toutes les projets collectibles où les NFT peuvent être utilisés comme photo de profil sur les réseaux
                    </p>
                </li>

                <li>
                    <a href="#" class="lexique_mot">PLAY TO EARN</a>
                    <p class="lexique_definition">
                    Jouer pour gagner. Jeu vidéo qui fait gagner des cryptos ou NFT donc de l’argent
                    </p>
                </li>

                <li>
                    <a href="#" class="lexique_mot">POOL</a>
                    <p class="lexique_definition">
                    Coffre virtuel où l’on dépose son argent pour qu’il fructifie. On en trouve dans de nombreux protocoles de DeFi
                    </p>
                </li>

                <li>
                    <a href="#" class="lexique_mot">PROOF OF STAKE</a>
                    <p class="lexique_definition">
                    Le Proof of stake, preuve d’enjeu en français, est un des mécanismes de consensus blockchain les plus répandus. L’idée de cet algorithme est d’attribuer la détermination du consensus par l’ajout de blocs à la blockchain à des nœuds appelés masternodes qui détiennent un certain montant des coins de la blockchain en question
                    </p>
                </li>

                <li>
                    <a href="#" class="lexique_mot">PROOF OF WORK</a>
                    <p class="lexique_definition">
                    La Proof of work, preuve de travail en français, est un des deux grands algorithmes de consensus pour blockchain. C’est aussi le plus ancien, puisque Bitcoin se base sur ce protocole de consensus. Dans une blockchain publique, un algorithme de consensus sert à désigner le bloc qui est ajouté à la blockchain à un instant T et donc la “vérité” de cette blockchain
                    </p>
                </li>

                <li>
                    <a href="#" class="lexique_mot">PUMP</a>
                    <p class="lexique_definition">
                    Mouvement haussier soudain du prix d’une crypto-monnaie ou d’une collection de NFT
                    </p>
                </li>

                <li>
                    <h3 class="lexique_alphabet">R</h3>

                    <a href="#" class="lexique_mot">REKT</a>
                    <p class="lexique_definition">
