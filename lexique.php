                    </p>
                </li>

                <li>
                    <a href="#" class="lexique_mot">ROADMAP</a>
                    <p class="lexique_definition">
                    Plan de réalisation étape par étape d’un projet NFT. Le concept de Roadmap était introduit par le projet Bored Ape Yacht Club
                    </p>
                </li>

                <li>
                    <a href="#" class="lexique_mot">ROYALTIES</a>
